//======================================================================================================================
// Value Not Empty
//======================================================================================================================

export const valueNotEmpty = <TValue>(
  value: TValue | null | undefined
): value is TValue => {
  return value !== null && value !== undefined;
};

//======================================================================================================================
// Find Matching Values
//======================================================================================================================

export const findMatchingValues = (array1: any, array2: any) => {
  const matching = [];
  for (let i = 0; i < array1.length; i++) {
    if (array2.indexOf(array1[i]) > -1) {
      matching.push(array1[i]);
    }
  }
  return matching;
};

//======================================================================================================================
// Random Value
//======================================================================================================================

export function randomValue<V = any>(array: V[]): V {
  return array[Math.floor(Math.random() * array.length)];
}
